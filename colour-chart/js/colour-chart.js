function lightBackground(){
  document.getElementById('switching').style.backgroundImage = "radial-gradient(#ffffff, #e4e4e4)";
  document.getElementById('title').style.color = "#1a3d5a";
}

function darkBackground(){
  document.getElementById('switching').style.backgroundImage = "radial-gradient(#1a3d5a, #0c1f2f)";
  document.getElementById('title').style.color = "#fff";
}